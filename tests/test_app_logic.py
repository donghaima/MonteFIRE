# ── Chart helpers (no Streamlit required) ─────────────────────────────────────

class TestChartHelpers:
    def _trajectory_chart(self, result):
        """Inline copy of _trajectory_chart() from app.py to test without st."""
        import plotly.graph_objects as go
        ages = result["ages"]
        med  = result["median_trajectory"]
        p10  = result["p10_trajectory"]
        p90  = result["p90_trajectory"]

        fig = go.Figure()
        fig.add_trace(go.Scatter(
            x=ages + ages[::-1], y=p90 + p10[::-1],
            fill="toself", fillcolor="rgba(59,130,246,0.12)",
            line=dict(color="rgba(0,0,0,0)"), name="band",
        ))
        fig.add_trace(go.Scatter(x=ages, y=p10, name="p10"))
        fig.add_trace(go.Scatter(x=ages, y=p90, name="p90"))
        fig.add_trace(go.Scatter(x=ages, y=med, name="Median"))
        return fig

    def _make_result(self, n=41):
        ages = list(range(50, 50 + n))
        return {
            "ages": ages,
            "median_trajectory":  [1_500_000 - i * 10_000 for i in range(n)],
            "p10_trajectory":     [1_000_000 - i * 15_000 for i in range(n)],
            "p90_trajectory":     [2_200_000 + i * 5_000  for i in range(n)],
            "median_taxes":       [8_000.0] * n,
            "median_healthcare":  [5_000.0] * n,
            "success_rate": 0.85,
        }

    def test_trajectory_chart_has_four_traces(self):
        result = self._make_result()
        fig = self._trajectory_chart(result)
        assert len(fig.data) == 4

    def test_band_polygon_length_correct(self):
        result = self._make_result(41)
        fig = self._trajectory_chart(result)
        band = fig.data[0]
        assert len(band.x) == 41 * 2   # forward + backward ages
        assert len(band.y) == 41 * 2

    def test_median_line_length_matches_ages(self):
        result = self._make_result(41)
        fig = self._trajectory_chart(result)
        median_trace = fig.data[3]
        assert len(median_trace.x) == 41
        assert len(median_trace.y) == 41

    def test_aca_cliff_detection(self):
        """ACA cliff logic: max jump > $5k in healthcare trajectory."""
        hc = [3_000.0] * 10 + [22_000.0] + [22_000.0] * 5  # jump at index 10
        max_jump = max(abs(hc[i] - hc[i - 1]) for i in range(1, len(hc)))
        assert max_jump > 5_000

    def test_no_aca_cliff_when_smooth(self):
        hc = [5_000.0 + i * 50 for i in range(20)]  # gradual increase
        max_jump = max(abs(hc[i] - hc[i - 1]) for i in range(1, len(hc)))
        assert max_jump <= 5_000


# ── Real-return and withdrawal-rate display logic ─────────────────────────────

class TestDisplayMetrics:
    def test_real_return_calculation(self):
        sp = {"mean_annual_return": 7.0, "inflation_rate": 3.0}
        real_return = sp["mean_annual_return"] - sp["inflation_rate"]
        assert real_return == pytest.approx(4.0)

    def test_withdrawal_rate_4pct_rule(self):
        net_from_portfolio = 80_000
        total_net_worth = 2_000_000
        wr = net_from_portfolio / total_net_worth * 100
        assert wr == pytest.approx(4.0)
        assert wr <= 4.0   # exactly at the rule

    def test_withdrawal_rate_above_4pct(self):
        net_from_portfolio = 100_000
        total_net_worth = 2_000_000
        wr = net_from_portfolio / total_net_worth * 100
        assert wr > 4.0


# ── End-to-end: full data flow through engine ─────────────────────────────────

class TestEndToEnd:
    def test_full_pipeline(self, portfolio_state, default_cash_flows, default_sim_params):
        """ETL output → SimulationParams → engine.run() → result dict."""
        from engine import SimulationParams
        from engine.monte_carlo import run as run_engine

        active = default_cash_flows[default_cash_flows["Active"]]
        expenses = float(active[active["Category"] != "Income"]["Annual ($)"].sum())
        income   = float(active[active["Category"] == "Income"]["Annual ($)"].sum())
        sp = default_sim_params

        params = SimulationParams.from_portfolio_state(
            portfolio_state,
            overrides={
                "annual_spending_today":     expenses,
                "social_security_annual":    income,
                "social_security_start_age": int(sp["social_security_start_age"]),
                "mean_annual_return":        sp["mean_annual_return"] / 100.0,
                "return_std_dev":            sp["return_std_dev"] / 100.0,
                "inflation_rate":            sp["inflation_rate"] / 100.0,
                "plan_to_age":               int(sp["plan_to_age"]),
                "num_iterations":            int(sp["num_iterations"]),
                "filing_status":             sp["filing_status"],
                "household_size":            int(sp["household_size"]),
            },
        )

        result = run_engine(params, config_dir=CONFIG_DIR, seed=42)

        # Structural checks
        assert "success_rate" in result
        assert "median_trajectory" in result
        assert "median_taxes" in result
        assert "median_healthcare" in result
        assert len(result["ages"]) == (sp["plan_to_age"] - int(params.current_age) + 1)

        # With $1.4M portfolio and $80k spending, should have reasonable success rate
        assert result["success_rate"] >= 0.0
        assert result["success_rate"] <= 1.0
