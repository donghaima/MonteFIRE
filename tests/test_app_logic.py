# ── Cash flow aggregation logic ───────────────────────────────────────────────

class TestCashFlowAggregation:
    def _aggregate(self, cf: pd.DataFrame):
        active = cf[cf["Active"]]
        expenses = float(active[active["Category"] != "Income"]["Annual ($)"].sum())
        income   = float(active[active["Category"] == "Income"]["Annual ($)"].sum())
        return expenses, income

    def test_only_active_rows_counted(self, default_cash_flows):
        expenses, income = self._aggregate(default_cash_flows)
        assert expenses == 80_000   # 60k + 8k + 12k
        assert income == 0.0        # SS row is inactive

    def test_toggle_ss_on_changes_income(self, default_cash_flows):
        cf = default_cash_flows.copy()
        cf.loc[cf["Name"].str.contains("Social Security"), "Active"] = True
        expenses, income = self._aggregate(cf)
        assert income == 28_000

    def test_net_equals_expenses_minus_income(self, default_cash_flows):
        expenses, income = self._aggregate(default_cash_flows)
        net = expenses - income
        assert net == expenses  # income inactive

    def test_all_inactive_returns_zeros(self, default_cash_flows):
        cf = default_cash_flows.copy()
        cf["Active"] = False
        expenses, income = self._aggregate(cf)
        assert expenses == 0.0
        assert income == 0.0

    def test_discretionary_counted_as_expense(self, default_cash_flows):
        # "Travel" is Discretionary, not Income — should be in expenses
        active = default_cash_flows[default_cash_flows["Active"]]
        travel_rows = active[active["Name"] == "Travel"]
        assert len(travel_rows) == 1
        expenses, _ = self._aggregate(default_cash_flows)
        assert expenses >= 12_000


